// TSH
function TrueFalseCheckTSH()
	{
		if (document.getElementById('TrueTSHCheck').checked)
			{
				document.getElementById('ifTSHTrue').style.visibility = 'visible';
			}
		else document.getElementById('ifTSHTrue').style.visibility = 'hidden';
	}

